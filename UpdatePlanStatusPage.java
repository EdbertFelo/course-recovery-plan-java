    private void Save_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Save_ButtonActionPerformed
    String planId = PlanID_Field.getText().trim();
    String newStatus = Status_Field.getText().trim(); // rename this field later
    if (planId.isEmpty() || newStatus.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Fields cannot be empty.");
        return;
    }
    java.util.List<String> lines = new java.util.ArrayList<>();
    boolean found = false;
    try (java.io.BufferedReader br = new java.io.BufferedReader(new java.io.FileReader("recovery_plans.csv"))) {
        String line;
        while ((line = br.readLine()) != null) {
            String[] data = line.split(",");
            if (data[0].equals(planId)) {
                found = true;
                if (data.length < 10) {
                    String[] expanded = new String[10];
                    System.arraycopy(data, 0, expanded, 0, data.length);
                    for (int i = data.length; i < 10; i++) expanded[i] = "";
                    data = expanded;
                }
                data[8] = newStatus; // ✅ UPDATE STATUS HERE
                line = String.join(",", data);
            }
            lines.add(line);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error reading file.");
        return;
    }
    if (!found) {
        JOptionPane.showMessageDialog(this, "Plan ID not found.");
        return;
    }
    try (java.io.FileWriter fw = new java.io.FileWriter("recovery_plans.csv")) {
        for (String l : lines) fw.write(l + "\n");
        JOptionPane.showMessageDialog(this, "Status updated.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error saving file.");
    }
    }//GEN-LAST:event_Save_ButtonActionPerformed

