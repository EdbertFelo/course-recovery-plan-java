    private User authenticateUser(String inputId, String inputPassword) {
        String filePath = "users.csv";

        try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
            String line;

            while ((line = br.readLine()) != null) {
                User user = User.fromCsv(line);
                if (user == null) continue;

                if (user.getuserID().equals(inputId) &&
                    user.getpassword().equals(inputPassword)) {

                    user.setlastLogin(java.time.LocalDateTime.now().toString());
                    return user;
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading users file");
        }

        return null;
    }

