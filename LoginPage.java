    private void Login_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login_ButtonActionPerformed
         String userID = UserID_Field.getText();
    String password = new String(jPassword_Field.getPassword());

    if (userID.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter both username and password.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    currentUser = authenticateUser(userID, password);

    if (currentUser != null) {

        switch (currentUser.getRole()) {
            case CourseAdministrator:
                new AdminPage(currentUser).setVisible(true);
                break;

            case AcademicOfficer:
                new OfficerPage(currentUser).setVisible(true);
                break;

            default:
                JOptionPane.showMessageDialog(this, "Unknown role for this user.");
                return;
        }

        this.dispose();
    } 
    else {
        JOptionPane.showMessageDialog(this, "Invalid Username or Password!", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_Login_ButtonActionPerformed

